for (var i = 0; i <= 20; i++) {
  console.log(i);
}

console.log('------------------------------');

for (var i = 20; i >= 0; i--) {
  console.log(i);
}

console.log('------------------------------');

for (var i = 0; i < 20; i = i + 2) {
  console.log(i);
}

console.log('------------------------------');

for (var i = 20; i > 0; i = i - 2) {
  console.log(i);
}
